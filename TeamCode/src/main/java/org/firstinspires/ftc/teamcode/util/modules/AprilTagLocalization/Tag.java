package org.firstinspires.ftc.teamcode.util.modules.AprilTagLocalization;

public class Tag {

    private double x;
    private double y;
    private double z;

    public Tag(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public double getZ() {
        return z;
    }
}
